package Model;

/**
 *
 * @author Eduardo
 */
public class Cliente {
    
    private int rg;
    private String nome;
    private int telefone;

    public Cliente() {
    }

    public int getRg() {
        return rg;
    }

    public void setRg(int rg) {
        this.rg = rg;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }
    
    
}
